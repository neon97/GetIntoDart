void switchCase() {
  //? can be used with object map and other datatypes tooo.
  var data = "Apple";
  switch (data) {
    case "Apple":
      print("Its a fruit");
      break;
    case "LadyFinger":
      print("Its a vegetable");
      break;
    case "Audi":
      print("Its a car");
      break;
    case "Hawaii":
      print("Its a place");
      break;
    default:
  }
}
